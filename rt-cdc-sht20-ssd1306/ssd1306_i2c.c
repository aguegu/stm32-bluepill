#include "ch.h"
#include "hal.h"
#include "string.h"
#include "ssd1306_i2c.h"

uint8_t raw[BUFFER_LEN_RAW];
uint8_t * buffer = raw + 3;

I2CDriver * i2cp;

void ssd1306_init(I2CDriver *i2c) {
  raw[0] = 0x80;
  raw[1] = 0xb0;
  raw[2] = 0x40;
  i2cp = i2c;

  i2cAcquireBus(i2cp);

  // uint8_t s[27] = {0x00, 0xae, 0xd5, 0x80, 0xa8, HEIGHT - 1, 0x40, 0x8d, 0x14, 0x20, 0x01, 0xa1, 0xc8, 0xda, 0x12, 0x81, 0x8f, 0xd9, 0x02, 0xdb, 0x40, 0xa4, 0xa6, 0x2e, 0xaf, 0x00, 0x10};
  uint8_t s[33] = {0x00, 0xae, 0xd5, 0x80, 0xa8, HEIGHT - 1, 0x40, 0x8d, 0x14, 0x20, 0x01, 0x21, 0, WIDTH - 1, 0x22, 0x00, (HEIGHT >> 3) - 1, 0xa0, 0xc0, 0xda, 0x02, 0x81, 0x8f, 0xd9, 0x02, 0xdb, 0x40, 0xa4, 0xa6, 0x2e, 0xaf, 0x00, 0x10};

  i2cMasterTransmit(i2cp, 0x3c,
                          s, 33, NULL, 0);

  i2cReleaseBus(i2cp);
}

void ssd1306_display() {
  i2cAcquireBus(i2cp);

  i2cMasterTransmit(i2cp, 0x3c,
                        raw, BUFFER_LEN_RAW, NULL, 0);
  i2cReleaseBus(i2cp);
}

void ssd1306_clear(void) {
  memset(buffer, 0x00, BUFFER_LEN);
}
